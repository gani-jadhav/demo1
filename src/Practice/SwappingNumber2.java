package Practice;

public class SwappingNumber2 {
    public static void main(String[] args) {
        String[] s = {"shyam","dj","delhi","dj"};
        int count = 0;
        for(int i = 0; i < s.length; i++) {
            if(s[i].equals("shyam")) {
                count++;
            }
        }
        System.out.println("count of shyam is = " +count);
    }
}










